# Post-training eval (Indian law LLM-as-judge)
# ─────────────────────────────────────────────

def evaluate_indian_law(model, tokenizer, test_raw, adapter_path: str, tier: dict,
                        groq_api_key: str | None = None):
    """
    LLM-as-judge with an Indian law-specific rubric.
    The judge checks: correct statute (BNS vs IPC), section accuracy, SC precedents.
    """
    try:
        from groq import Groq
    except ImportError:
        print("Skipping eval: 'groq' not installed. pip install groq")
        return {}

    api_key = groq_api_key or os.environ.get("GROQ_API_KEY")
    if not api_key:
        print("Skipping eval: GROQ_API_KEY not set.")
        return {}

    from tqdm import tqdm

    groq_client = Groq(api_key=api_key)
    judge_model = "llama-3.3-70b-versatile"
    max_eval    = min(100, len(test_raw))
    samples     = test_raw.select(range(max_eval))

    model.eval()
    scores  = []
    records = []

    for sample in tqdm(samples, desc="Indian law LLM-judge eval"):
        question     = sample.get("question", "").strip()
        ground_truth = sample.get("answer", "").strip()

        # Generate response
        messages  = [{"role": "system", "content": INDIAN_LAW_SYSTEM},
                     {"role": "user",   "content": question}]
        formatted = tokenizer.apply_chat_template(
            messages, tokenize=False, add_generation_prompt=True
        )
        inputs    = tokenizer(formatted, return_tensors="pt").to(model.device)
        input_len = inputs["input_ids"].shape[1]

        with torch.no_grad():
            out = model.generate(
                **inputs, max_new_tokens=350, temperature=0.1,
                top_p=0.9, do_sample=True, pad_token_id=tokenizer.eos_token_id,
            )
        response = tokenizer.decode(out[0][input_len:], skip_special_tokens=True).strip()

        judge_prompt = f"""You are an expert evaluator of Indian law responses. Rate this AI response on a 1-5 scale.

Question: {question[:400]}

Reference answer: {ground_truth[:500]}

AI response: {response[:500]}

{INDIAN_LAW_JUDGE_RUBRIC}

Respond with ONLY a single digit (1-5). No explanation."""

        try:
            completion = groq_client.chat.completions.create(
                model=judge_model,
                messages=[{"role": "user", "content": judge_prompt}],
                max_tokens=5, temperature=0,
            )
            score_text = completion.choices[0].message.content or ""
            m = re.search(r'[1-5]', score_text.strip())
            score = int(m.group()) if m else 3
        except Exception as e:
            print(f"Groq error: {e}")
            score = 3

        scores.append(score)
        records.append({"question": question[:80], "score": score,
                        "response": response[:150]})

    avg_score  = sum(scores) / len(scores) if scores else 0.0
    score_dist = {str(i): scores.count(i) for i in range(1, 6)}

    results = {
        "phase":      "phase3b_indian_law",
        "gpu":        tier["gpu_name"],
        "vram_gb":    round(tier["vram_gb"], 1),
        "tier":       tier["label"],
        "model":      BASE_MODEL,
        "adapter":    adapter_path,
        "dataset":    PRIMARY_DATASET,
        "judge":      judge_model,
        "lora_r":     LORA_R,
        "num_eval":   len(scores),
        "avg_score":  round(avg_score, 3),
        "score_dist": score_dist,
    }

    print(f"\n{'='*50}")
    print(f"INDIAN LAW LLM-JUDGE ({judge_model})")
    print(f"Average score: {avg_score:.2f}/5.0")
    print(f"Distribution:  {score_dist}")
    print(f"Baseline (Mistral base on Indian law): ~2.5/5.0")
    print(f"Target: ≥3.5/5.0 (+1.0 pts improvement)")
    print(f"{'='*50}")

    out_path = os.path.join(OUTPUT_DIR, "eval_results.json")
    with open(out_path, "w") as f:
        json.dump(results, f, indent=2)
    print(f"Results saved: {out_path}")
    return results


