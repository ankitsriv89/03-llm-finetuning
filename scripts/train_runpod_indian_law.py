"""
train_runpod_indian_law.py — Phase 3b Indian Law, GPU-agnostic for RunPod
==========================================================================
Fine-tunes Mistral-7B-Instruct-v0.2 on Indian legal Q&A for JusticeAI alignment.

  Primary (~60%):  viber1/indian-law-dataset
                   Indian law Q&A covering IPC/BNS, CPC/BNSS, Constitution, BSA
                   Format: {question, answer}
                   NOTE: Pre-2024 vintage. Cites repealed IPC sections.

  Auxiliary (25%): Exploration-Lab/InLegalNLI
                   NLI pairs from Indian SC/HC judgments
                   Reformatted as legal reasoning Q&A
                   NOTE: Pre-2024 jurisprudence — still authoritative as precedent.

  BNS map (15%):   data/bns_bnss_bsa_mapping.jsonl  (locally generated)
                   IPC→BNS / CrPC→BNSS / IEA→BSA mappings
                   ~800 samples from 109 hand-curated seed mappings
                   Run scripts/build_bns_mapping_dataset.py to (re)build it.
                   Teaches model the post-July-2024 statute numbers it would
                   otherwise never see in the larger pre-2024 datasets.

Why r=32 (not r=16):
  Indian law is a specialized distribution far from Mistral's pretraining.
  The larger adapter rank captures more domain-specific legal reasoning
  without increasing inference cost (adapters are merged at deployment).

JusticeAI alignment:
  This adapter teaches BNS/BNSS/BSA section references (post-July 2024 codes),
  Indian SC judgment reasoning style, and bail-specific legal analysis —
  directly feeding into JusticeAI Phase 1 (bail decisions).

GPU tiers (seq_len=2048, r=32 adapter is slightly larger than r=16):

    VRAM >= 70GB  (A100 80GB, H100 80GB):
        bf16 LoRA, batch=8,  no grad ckpt, flash-attn2  → ~25 min

    VRAM >= 40GB  (A40/L40S/A6000 48GB):
        bf16 LoRA, batch=4,  no grad ckpt, flash-attn2  → ~40 min

    VRAM >= 22GB  (RTX 4090 / A5000 24GB):
        4-bit QLoRA, batch=4, grad ckpt on, flash-attn2 → ~70 min

    VRAM < 22GB   (T4 16GB, etc.):
        4-bit QLoRA, batch=2, grad ckpt on, no flash-attn → ~140 min

Setup on RunPod:
    cd /workspace && git clone <repo> jobs-prjcts && cd jobs-prjcts/03-llm-finetuning
    pip install -r requirements_runpod.txt
    huggingface-cli login
    python scripts/train_runpod_indian_law.py
"""

