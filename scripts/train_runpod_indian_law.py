def format_indian_law_sample(sample: dict, tokenizer) -> str:
    """
    viber1/indian-law-dataset fields: {question, answer}

    The system prompt explicitly references BNS/BNSS/BSA so the model learns
    to prefer new statute citations for post-2024 questions while correctly
    referencing IPC/CrPC for historical/precedent context.
    """
    question = sample.get("question", "").strip()
    answer   = sample.get("answer", "").strip()

    messages = [
        {"role": "system",    "content": INDIAN_LAW_SYSTEM},
        {"role": "user",      "content": question},
        {"role": "assistant", "content": answer},
    ]
    return tokenizer.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=False
    )


def format_bns_mapping_sample(sample: dict, tokenizer) -> str:
    """
    BNS/BNSS/BSA mapping samples are simple {question, answer} pairs generated by
    build_bns_mapping_dataset.py. Uses the same system prompt as the primary dataset
    to keep formatting consistent — the model learns these are authoritative Indian
    law answers with correct post-2024 statute references.
    """
    question = sample.get("question", "").strip()
    answer   = sample.get("answer", "").strip()

    messages = [
        {"role": "system",    "content": INDIAN_LAW_SYSTEM},
        {"role": "user",      "content": question},
        {"role": "assistant", "content": answer},
    ]
    return tokenizer.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=False
    )


def format_inlegalnli_sample(sample: dict, tokenizer) -> str:
    """
    Exploration-Lab/InLegalNLI fields:
        premise    : Indian SC/HC judgment excerpt
        hypothesis : a legal claim about the judgment
        label      : 0=entailment, 1=neutral, 2=contradiction

    Reformatted as a legal reasoning task:
    "Does the following court judgment support this legal claim? Explain why."

    This teaches the model to reason about Indian court language and precedent,
    which directly supports JusticeAI's citation-grounded bail analysis.
    """
    premise    = sample.get("premise", "").strip()
    hypothesis = sample.get("hypothesis", "").strip()
    label_id   = sample.get("label", 1)

    label_map = {0: "Yes, the judgment supports this claim",
                 1: "The judgment neither clearly supports nor contradicts this claim",
                 2: "No, the judgment contradicts this claim"}
    label_text = label_map.get(label_id, label_map[1])

    # Truncate long judgment excerpts to fit within seq budget
    if len(premise) > 1200:
        premise = premise[:1200] + "..."

    user_content = (
        f"Court Judgment Excerpt:\n{premise}\n\n"
        f"Legal Claim: {hypothesis}\n\n"
        f"Does the judgment support this legal claim? Explain your reasoning."
    )
    # Generate a short but informative answer from the label
    answer = (
        f"{label_text}.\n\n"
        f"The judgment excerpt {'explicitly establishes' if label_id == 0 else 'does not establish' if label_id == 2 else 'does not directly address'} "
        f"the proposition that {hypothesis.lower().rstrip('.')}."
    )

    messages = [
        {"role": "system",    "content": INDIAN_LAW_SYSTEM},
        {"role": "user",      "content": user_content},
        {"role": "assistant", "content": answer},
    ]
    return tokenizer.apply_chat_template(
        messages, tokenize=False, add_generation_prompt=False
    )


def _load_bns_mapping_dataset(tokenizer):
    """
    Load the locally-generated BNS/BNSS/BSA mapping dataset.
    Returns None if the file is missing (allows training to proceed without it,
    but logs a strong warning — JusticeAI critically depends on this dataset).
    """
    # Resolve path relative to script's parent (the project root)
    script_dir   = os.path.dirname(os.path.abspath(__file__))
    project_root = os.path.dirname(script_dir)
    bns_path     = os.path.join(project_root, BNS_DATASET_PATH)

    if not os.path.exists(bns_path):
        print(f"\n{'!'*70}")
        print(f"WARNING: BNS mapping dataset not found at {bns_path}")
        print(f"Run: python scripts/build_bns_mapping_dataset.py")
        print(f"Without this, model will cite REPEALED IPC/CrPC sections in production.")
        print(f"{'!'*70}\n")
        return None

    print(f"Loading BNS mapping dataset: {bns_path}")
    bns_raw = load_dataset("json", data_files=bns_path, split="train")
    bns_formatted = bns_raw.map(
        lambda s: {"text": format_bns_mapping_sample(s, tokenizer)},
        remove_columns=bns_raw.column_names,
    )
    print(f"BNS mapping — {len(bns_formatted)} samples loaded "
          f"(post-July-2024 statute references)")
    return bns_formatted


def load_data(tokenizer):
    """
    Build a 3-way training mix:
        Primary    (~60%):  viber1/indian-law-dataset    — broad Indian law Q&A
        Auxiliary  (~25%):  Exploration-Lab/InLegalNLI   — SC/HC reasoning
        BNS map    (~15%):  local bns_bnss_bsa_mapping.jsonl — post-2024 statute updates

    The BNS mapping is small (~800 samples) but high-signal: it directly corrects the
    pre-2024 framing baked into the larger datasets. Training sees it across multiple
    epochs (NUM_EPOCHS=4) so the model genuinely learns BNS section numbers.
    """
    print(f"Loading primary dataset: {PRIMARY_DATASET}...")
    raw   = load_dataset(PRIMARY_DATASET, split="train")
    split = raw.train_test_split(test_size=TEST_SIZE, seed=42)
    train_raw, test_raw = split["train"], split["test"]
    print(f"Primary — Train: {len(train_raw)} | Test: {len(test_raw)}")

    train_primary = train_raw.map(
        lambda s: {"text": format_indian_law_sample(s, tokenizer)},
        remove_columns=train_raw.column_names,
    )

    components: list = [("primary", train_primary, 1.0 - AUX_WEIGHT - BNS_WEIGHT)]

    # Auxiliary: InLegalNLI
    print(f"Loading auxiliary dataset: {AUX_DATASET} ({AUX_SAMPLES} samples)...")
    try:
        aux_raw = load_dataset(AUX_DATASET, split="train")
        aux_raw = aux_raw.select(range(min(AUX_SAMPLES, len(aux_raw))))
        train_aux = aux_raw.map(
            lambda s: {"text": format_inlegalnli_sample(s, tokenizer)},
            remove_columns=aux_raw.column_names,
        )
        print(f"Auxiliary (InLegalNLI) — {len(train_aux)} samples loaded")
        components.append(("aux", train_aux, AUX_WEIGHT))
    except Exception as e:
        print(f"WARNING: Could not load auxiliary dataset ({e}). Skipping.")

    # BNS/BNSS/BSA mapping (local file)
    bns_dataset = _load_bns_mapping_dataset(tokenizer)
    if bns_dataset is not None:
        components.append(("bns_map", bns_dataset, BNS_WEIGHT))

    # Re-normalise weights if any component was skipped
    total_weight = sum(w for _, _, w in components)
    components = [(name, ds, w / total_weight) for name, ds, w in components]

    # Pick the anchor: the smallest dataset relative to its weight defines the
    # achievable mix size. Each component is then sampled to match its weight.
    achievable_sizes = [len(ds) / w for _, ds, w in components if w > 0]
    anchor_size      = min(achievable_sizes)

    chunks = []
    summary_parts = []
    for name, ds, w in components:
        n = min(int(anchor_size * w), len(ds))
        # If the BNS mapping is small, oversample by repeating (small but critical)
        if name == "bns_map" and n < len(ds):
            n = len(ds)   # use all of it — never drop BNS samples
        chunks.append(ds.select(range(min(n, len(ds)))))
        summary_parts.append(f"{n} {name}")

    mixed = concatenate_datasets(chunks).shuffle(seed=42)
    print(f"\nMixed dataset: {len(mixed)} samples ({' + '.join(summary_parts)})")
    weight_summary = ", ".join(f"{name}={w:.0%}" for name, _, w in components)
    print(f"Target weights: {weight_summary}\n")

    return mixed, test_raw


