BASE_MODEL      = "mistralai/Mistral-7B-Instruct-v0.2"
PRIMARY_DATASET = "viber1/indian-law-dataset"
AUX_DATASET     = "Exploration-Lab/InLegalNLI"
BNS_DATASET_PATH = "data/bns_bnss_bsa_mapping.jsonl"   # local file generated by build_bns_mapping_dataset.py
OUTPUT_DIR      = "/workspace/outputs/phase3b-indian-law"

NUM_EPOCHS      = 4
LEARNING_RATE   = 1.0e-4
MAX_SEQ_LENGTH  = 2048
LOGGING_STEPS   = 10
SAVE_STEPS      = 100

LORA_R          = 32           # higher rank — larger domain gap from Mistral's training
LORA_ALPHA      = 64
LORA_DROPOUT    = 0.05
TARGET_MODULES  = ["q_proj", "k_proj", "v_proj", "o_proj",
                   "gate_proj", "up_proj", "down_proj"]

TEST_SIZE       = 0.10
AUX_SAMPLES     = 3000         # InLegalNLI samples to mix in
AUX_WEIGHT      = 0.25         # 25% of mixed dataset from InLegalNLI
BNS_WEIGHT      = 0.15         # 15% of mixed dataset from BNS/BNSS/BSA mapping
# ^ The BNS mapping dataset (~800 samples) is small but critical: it teaches the
#   model that IPC §302 → BNS §103, etc. Without it, the model would confidently
#   cite repealed colonial-era sections for post-July-2024 cases (a hard failure
#   mode for JusticeAI). 15% weight = high signal-to-sample ratio.

INDIAN_LAW_SYSTEM = (
    "You are an expert in Indian law with deep knowledge of the Bharatiya Nyaya Sanhita "
    "(BNS, 2023), Bharatiya Nagarik Suraksha Sanhita (BNSS, 2023), Bharatiya Sakshya "
    "Adhiniyam (BSA, 2023), the Constitution of India, and Indian Supreme Court and High "
    "Court jurisprudence. When answering legal questions, cite the correct current statutes "
    "(BNS/BNSS/BSA for post-July 2024 matters, IPC/CrPC/IEA for historical cases), "
    "reference relevant Supreme Court precedents where applicable, and provide accurate, "
    "precise legal analysis. Clearly distinguish between the old codes (IPC/CrPC) and the "
    "new codes (BNS/BNSS/BSA) when the distinction is legally material."
)

# Rubric used in the LLM judge prompt — also embedded in evaluate.py
INDIAN_LAW_JUDGE_RUBRIC = """Rating criteria for Indian law responses:
1 — Wrong statute cited, incorrect legal principle, or factually wrong
2 — Correct general area but wrong section numbers or significant procedural errors
3 — Correct answer but missing key nuance (e.g. old vs new code distinction)
4 — Correct, proper citations, minor omissions in nuance or case law
5 — Accurate, correct BNS/BNSS/BSA vs IPC/CrPC distinction, relevant SC precedents cited"""


